%--------------------------------------------------------------------------
%   h_output = exp_wave(T,fp,fs)
%   T  信号长度
%   fp 点频信号频率
%   fs 生成速率，按照多少速率来生成信号
%--------------------------------------------------------------------------
%   example
%   exp_wave(20e-6,10e6,100e6)
%--------------------------------------------------------------------------
function sig = exp_wave(T,fp,fs)
t = (0:1/fs:T-1/fs)';
sig = exp(1j*2*pi*fp*t);
end
